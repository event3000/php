// Это должна быть одна функция 

function conv_str($str,$selector) {

  switch ($selector) {
    case "up":
      return strtoupper($str) ; 
      break;

    case "lw":
      return strtolower($str) ; 
      break;

    case "lw_uc":
      return ucwords(strtolower($str)) ; 
      break;      
  }
}

